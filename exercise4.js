// Check if a number is positive or negative
// Given num = -5, use if else to log "Positive" or "Negative".
let num = -5;
if (num > 0) {
    console.log("Positive");
} else {
    console.log("Negative");
}

// Check if a person is an adult
// Given age = 16, log "Adult" if age ≥ 18, otherwise log "Minor".
