// Arithmetic – Basic addition
// Declare num1 = 10 and num2 = 5. Calculate and log their sum.
let num1 = 10;
let num2 = 5;
let sum = num1 + num2;
console.log(sum);

// Arithmetic – Division
// Given total = 100 and items = 4, calculate average and log it.
let total = 100;
let items = 4;
let average = total / items;
console.log(average);

// Arithmetic – Modulus (%)
// Given let x = 17 and let y = 4, log the remainder of x % y.
let x = 17;
let y = 4;
let remainder = x % y;
console.log(remainder);

// Arithmetic – Exponent
// Declare base = 3 and power = 4.
// Use ** to compute 3^4 and log the result.
let base = 3;
let power = 4;
let exponent = base ** power;
console.log(exponent);

// Assignment – Add and assign
// Start with let score = 50;
// Use += to add 20 to it and log the updated score.
